numero1 = int(input("Digite o primeiro numero:"))
sla = int(input("Digite o segundo numero:"))

if numero1 == sla:
    print("os numeros sao iguais")

elif numero1 > sla:
    print(f"esse numero {numero1} e maior que {sla}")

else:
    print(f"erro! numero  {sla}  maior que {numero1}")
    
